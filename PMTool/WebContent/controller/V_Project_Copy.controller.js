            f_GotoProj: function() {
            //This code was generated by the layout editor.
            var oHistory = History.getInstance();
            var sPreviousHash = oHistory.getPreviousHash();
            // Go one screen back if you find a Hash
            if (sPreviousHash !== undefined) {
                window.history.go(-1);
            } // If you do not find a correct Hash, go to the Source screen using default router;
            else {
                var oRouter = sap.ui.core.UIComponent.getRouterFor(this);
                oRouter.navTo("", true);
            }
        },
           fSaveProj: function() {
            /*create operation*/
            var oModel = this.getView().getModel();
            var oEntry = {};
            oEntry.ZprojId = this.getView().byId("idProjId").getValue();
            oEntry.ZprDesc = this.getView().byId("idProjDesc").getValue();
            oEntry.ZprStdate = this.getView().byId("idProjStart").getValue() + "T00:00:00";
            oEntry.ZprEndate = this.getView().byId("idProjEnd").getValue() + "T00:00:00";
            oModel.create("/ProjMasterSet", oEntry, {
                method: "POST",
                success: function(data) {
                    MessageToast.show("Record has been saved");
                },
                error: function(e) {
                    MessageToast.show("Error while saving the record");
                }
            });
        }       

